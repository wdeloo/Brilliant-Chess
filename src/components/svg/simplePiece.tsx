export default function SimplePieceSVG({ piece, color, size, dataset, className, style, draggable, title }: { piece: PieceSymbol, color: Color, size: number, dataset?: Dataset, className?: string, style?: React.CSSProperties, draggable?: boolean, title?: string }) {
    const propsDataset = getPropsDataset(dataset ?? {})
    const pieceProps = {color, size}

    return (
        <div title={title} draggable={draggable} {...propsDataset} className={className} style={style}>
            {(() => {
                switch (piece) {
                    case PAWN:
                        return <Pawn {...pieceProps} />
                    case BISHOP:
                        return <Bishop {...pieceProps} />
                    case KNIGHT:
                        return <Knight {...pieceProps} />
                    case ROOK:
                        return <Rook {...pieceProps} />
                    case QUEEN:
                        return <Queen {...pieceProps} />
                }
            })()}
        </div>
    )
}